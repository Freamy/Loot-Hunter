package org.freamcoding.template.font;

public class Letter {
	
	public float x;
	public float y;
	public int id;
	public float sizeX;
	public float sizeY;
	
	public Letter(float x, float y, float sizeX, float sizeY, int id){
		this.x = x;
		this.y = y;
		this.sizeX = sizeX;
		this.sizeY = sizeY;
		this.id = id;
	}
}
